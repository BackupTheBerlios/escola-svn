package br.ufg.inf.apsi.escola.componentes.pessoa.modelo.excecoes;
/**
 * Classe definida para modelar os tipos de Endereço que um Pessoa pode possuir.
 * Pode ser por exemplo: Comercial ou Residencial.
 * @author gilmar
 *
 */
public enum TipoEndereco {
	/**
	 * tipo de endereço residencial
	 */
	RESIDENCIAL,
	/**
	 * tipo de endereço comercial
	 */
	COMERCIAL
}
